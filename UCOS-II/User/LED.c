void disbuffer_exchange(void)
{
    if (draw_buffer == buffer_A)
    {
        draw_buffer = buffer_B;
        disp_buffer = buffer_A;
    }
    else
    {
        draw_buffer = buffer_A;
        disp_buffer = buffer_B;
    }
	memcpy(draw_buffer, disp_buffer, LED_MAX_BYTES);
}


