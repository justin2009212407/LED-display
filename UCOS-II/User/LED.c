/*******************************************************************************
* Function Name  : mySysTick_Init
* Description    : This function init the SysTick.
* Input          : None
* Output         : None
* Return         : None
*******************************************************************************/
void HUB08_Init(void){

	GPIO_InitTypeDef GPIO_InitTypeStucture;

	RCC_APB2PeriphClockCmd(RCC_APB2Periph_GPIOA | RCC_APB2Periph_GPIOB
						 | RCC_APB2Periph_GPIOC,ENABLE);

	GPIO_InitTypeStucture.GPIO_Mode = GPIO_Mode_Out_PP;	//	推挽输出
	GPIO_InitTypeStucture.GPIO_Speed = GPIO_Speed_50MHz;

	/* CLK */
	GPIO_InitTypeStucture.GPIO_Pin = HUB08_CLK_PIN;
	GPIO_Init(HUB08_CLK_PORT,&GPIO_InitTypeStucture);

	/* R1 */
	GPIO_InitTypeStucture.GPIO_Pin = HUB08_R1_PIN;
	GPIO_Init(HUB08_R1_PORT,&GPIO_InitTypeStucture);

	/* R2 */
	GPIO_InitTypeStucture.GPIO_Pin = HUB08_R2_PIN;
	GPIO_Init(HUB08_R2_PORT,&GPIO_InitTypeStucture);

	/* G1 */
	GPIO_InitTypeStucture.GPIO_Pin = HUB08_G1_PIN;
	GPIO_Init(HUB08_G1_PORT,&GPIO_InitTypeStucture);

	/* G2 */
	GPIO_InitTypeStucture.GPIO_Pin = HUB08_G2_PIN;
	GPIO_Init(HUB08_G2_PORT,&GPIO_InitTypeStucture);

	/* LAT */
	GPIO_InitTypeStucture.GPIO_Pin = HUB08_LAT_PIN;
	GPIO_Init(HUB08_LAT_PORT,&GPIO_InitTypeStucture);

	/* OE */
	GPIO_InitTypeStucture.GPIO_Pin = HUB08_OE_PIN;
	GPIO_Init(HUB08_OE_PORT,&GPIO_InitTypeStucture);

	/* A */
	GPIO_InitTypeStucture.GPIO_Pin = HUB08_A_PIN;
	GPIO_Init(HUB08_A_PORT,&GPIO_InitTypeStucture);

	/* B */
	GPIO_InitTypeStucture.GPIO_Pin = HUB08_B_PIN;
	GPIO_Init(HUB08_B_PORT,&GPIO_InitTypeStucture);

	/* C */
	GPIO_InitTypeStucture.GPIO_Pin = HUB08_C_PIN;
	GPIO_Init(HUB08_C_PORT,&GPIO_InitTypeStucture);

	/* D */
	GPIO_InitTypeStucture.GPIO_Pin = HUB08_D_PIN;
	GPIO_Init(HUB08_D_PORT,&GPIO_InitTypeStucture);

	/* output value init */
	GPIO_ResetBits(HUB08_CLK_PORT, HUB08_CLK_PIN);	/* Reset clk */
	GPIO_ResetBits(HUB08_LAT_PORT, HUB08_LAT_PIN);	/* Reset latch */
	HUB08_selectNoline();							/* Do not select line */
	GPIO_ResetBits(HUB08_OE_PORT, HUB08_OE_PIN);	/* Enable OE */

	draw_buffer = buffer_A;
	disp_buffer = buffer_B;
	gColor = DOT_RED;

}
static void HUB08_selectNoline(void)
{
	HUB_LINE_PORT->ODR &= ~HUB_LINE_MASK;
}
static void HUB08_selectLine(uint8_t line)
{
	uint16_t tmp;

	tmp = HUB_LINE_PORT->IDR;
	tmp &= ~HUB_LINE_MASK;
	tmp |= (line<<HUB_LINE_MASK_SHIFT);
	HUB_LINE_PORT->ODR = tmp;

}


const uint8_t up_led_port_value[4] = {0x00, 0x01, 0x02, 0x03};
const uint8_t dn_led_port_value[4] = {0x00, 0x04, 0x08, 0x0c};

__inline static void HUB08_write(uint8_t up_dot, uint8_t dn_dot)
{

	HUB08_UP_PORT->ODR &= ~HUB08_UP_PIN_MASK;
	HUB08_UP_PORT->ODR |= up_led_port_value[up_dot];
	HUB08_DN_PORT->ODR &= ~HUB08_DN_PIN_MASK;
	HUB08_DN_PORT->ODR |= dn_led_port_value[dn_dot];
	GPIO_SetBits(HUB08_CLK_PORT, HUB08_CLK_PIN);
	GPIO_ResetBits(HUB08_CLK_PORT, HUB08_CLK_PIN);

}

void HUB08_scan(void)
{
	static uint8_t line = 0;

	uint8_t size;
	uint8_t *R1_buffer;
	uint8_t *R2_buffer;
	R1_buffer = disp_buffer + line * LED_HORISON_SIZE;
	R2_buffer = disp_buffer + (line + LED_SCAN_PERCENT)* LED_HORISON_SIZE;
	size = LED_HORISON_SIZE;

	while(size--)
	{
		HUB08_write(*R1_buffer++, *R2_buffer++);

	}
	GPIO_SetBits(HUB08_OE_PORT, HUB08_OE_PIN);	/* Disable OE */
	HUB08_selectNoline();

	GPIO_SetBits(HUB08_LAT_PORT, HUB08_LAT_PIN);	/* Shift-register data is stored in the storage register. */
	GPIO_ResetBits(HUB08_LAT_PORT, HUB08_LAT_PIN);

	HUB08_selectLine(line);							/* Select a line of LED */
	GPIO_ResetBits(HUB08_OE_PORT, HUB08_OE_PIN);	/* Enable OE */


	line ++;
	if (line == LED_SCAN_PERCENT) line = 0;
}

