void led_clear_area(short x,short y,short width, short height)
{
	short i,j;
	short offset;
	for (j = 0; j< height; j++)
	{
		offset = x + y*LED_HORISON_SIZE;
		for (i = 0; i< width; i++)
		{
			draw_buffer[offset++] = DOT_CLEAR;
		}
		y++;
	}
}
void led_picture(short x,short y,
                short width, short height, const uint8_t *pic)
{
    uint8_t mask;
    uint8_t temp;
    short x0,y0;
    short i,j;
    y0 = y;
    for(j = 0;j<height;j++){
        x0 = x;
        mask = 0;
        for(i = 0;i<width;i++){
            if(mask == 0){
//                if(i == 0)
//                    mask = 1 << ((width - 1) % 8);
//                else
//                    mask = 0x80;
				mask = 0x80;

                temp = *pic++;
            }
            if(temp & mask){
				if (x0 < LED_HORISON_SIZE && x0 > -1)
	                led_set_pix(x0,y0,gColor);
            }
            else{
				if (x0 < LED_HORISON_SIZE && x0 > -1)
	                led_clear_pix(x0,y0);
            }
            mask >>= 1;
            x0++;
        }
        y0++;
    }

}

void led_print(short x, short y, const char *str)
{
	char ch;
	const uint8_t *ch_base;
	short x0, y0;
	uint8_t ch_width;
	x0 = x;
	y0 = y;
	if (EN_WIDTH % 8 == 0)
	{
		ch_width = EN_WIDTH / 8;
	}
	else
	{
		ch_width = EN_WIDTH / 8 + 1;
	}

	while(*str != 0)
	{
		ch_base = ascii_font + (*str - ' ') * (EN_HIGHT * ch_width);
		led_picture(x0, y0,EN_WIDTH, EN_HIGHT, ch_base);
		x0 += EN_WIDTH;
		str++;
	}

}
